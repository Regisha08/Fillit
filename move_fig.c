#include "fillit.h"

char	*move_left_fig(char *fig)
{
	int i;

	i = 0;
	while (fig[0] == '.' && fig[5] == '.' && fig[10] == '.' && fig[15] == '.')
	{
		while (fig[i++] != '\0')
			if (fig[i] == '#' && fig[i - 1] == '.')
			{
				fig[i] = '.';
				fig[i - 1] = '#';
			}
		i = 0;
	}
	while (fig[0] == '.' && fig[1] == '.' && fig[2] == '.' && fig[3] == '.')
	{
		while (fig[i++] != '\0')
			if (fig[i] == '#' && fig[i - 5] == '.')
			{
				fig[i] = '.';
				fig[i - 5] = '#';
			}
		i = 0;
	}	
	return (fig);
}
